// Type-safe access helper
type TranslationSection = typeof translations;
type SectionKey = keyof TranslationSection;
type ItemKey<S extends SectionKey> = keyof TranslationSection[S];

// The main hook
export function useTranslation() {
  const { language } = useSettingsStore();
  const lang = language || 'id';

  function t<S extends SectionKey>(section: S, key: ItemKey<S>): string {
    const sectionData = translations[section] as any;
    const entry = sectionData?.[key as string];
    if (!entry) return String(key);
    return entry[lang] || entry['id'] || String(key);
  }

  return { t, lang };
}

// Convenience exports for direct access
export { translations };
export type { SectionKey, ItemKey };
